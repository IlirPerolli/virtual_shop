     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate(['name'=>'required|min:2|max:255']);
        $input = $request->all();
        City::create($input);
        session()->flash('added_city', 'City added sucessfully');
        return back();
    }

